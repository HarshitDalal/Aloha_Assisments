# Text type
string_v = 'Abc'
print(f'String : {string_v}')

# Numeric Type
int_v = 21
float_v = 45.6
complex_v = 3+4j
print(f'Int : {int_v} \nFloat : {float_v} \nComplex : {complex_v}')

# Sequence type
list_v = ['abc', 21, 45.6, 3+4j]
tuple_v = ('abc', 21, 45.6, 3+4j)
range_v = range(10)
print(f'List : {list_v} \nTuple : {tuple_v} \nRange : {range_v}')

# Mapping type
dict_v = {'name': 'abc', 'age': 21}
print(f'Dictionary : {dict_v}')

# Set type
set_v = {'abc', 21, 45.6}
frozenset_v = frozenset({'abc', 21, 45.6})
print(f'Set : {set_v} \nFrozenset : {frozenset_v}')

# Boolean Type
bool_v1 = True
bool_v2 = False
print(f'Bool : {bool_v1} , {bool_v2}')

# None Type
none_v = None
print(f'None : {none_v}')

